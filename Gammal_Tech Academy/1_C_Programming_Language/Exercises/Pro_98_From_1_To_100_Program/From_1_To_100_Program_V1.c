

/* >>> From 1 To 100 Program ( Version 1 ) <<< */

/* >>> A program that prints numbers from 1 to 100 (using while). <<< */

#include <stdio.h>

int main()
{
    printf("\n\nYou welcome in From 1 To 100 Program ( Version 1 ) ..\n\n");

    int i = 1;
    while (i <= 100)
    {
        printf("%d\n", i);
        i++;
    }
    printf("\n\n");

    return 0;
}