

/* >>> From 1 To 100 Program ( Version 1 ) <<< */

/* >>> A program that prints numbers from 1 to 100 (using do.. while ). <<< */

#include <stdio.h>

int main()
{
    printf("\n\nYou welcome in From 1 To 100 Program ( Version 1 ) ..\n\n");

    int i = 1;

    do
    {
        printf("\n%d", i);
        i++;

    } while (i <= 100);

    printf("\n\n\n");

    return 0;
}