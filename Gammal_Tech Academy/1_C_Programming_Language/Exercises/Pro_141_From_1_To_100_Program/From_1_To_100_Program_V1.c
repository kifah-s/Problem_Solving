

/* >>> From 1 To 100 Program ( Version 1 ) <<< */

/* >>> Program in which function her job is to print the numbers from 1 to 100. <<< */

#include <stdio.h>

int main()
{
    printf("\n\nYou welcome in From 1 To 100 Program ( Version 1 ) .. \n\n");

    for (int i = 1; i <= 100; i++)
    {
        printf("%d\n", i);
    }

    printf("\n\n");

    return 0;
}