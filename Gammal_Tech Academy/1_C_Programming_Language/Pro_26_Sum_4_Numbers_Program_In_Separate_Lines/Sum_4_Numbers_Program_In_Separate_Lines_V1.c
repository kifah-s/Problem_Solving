

/* >>> Sum 4 Numbers Program In Separate Lines ( Version 1 ) <<< */

/* >>> A program that records 4 digits and prints the sum of the first two digits in a line and the sum of the second two digits in a separate line <<< */

#include <stdio.h>

int main()
{
    printf("\n\nYou welcome in Sum 4 Numbers Program In Separate Lines ( Version 1 ) ..\n\n");

    int num_1 = 1;
    int num_2 = 2;
    int num_3 = 3;
    int num_4 = 4;

    printf("Num_1 + Num_2 = %d\n", num_1 + num_2);
    printf("Num_3 + Num_4 = %d\n\n\n", num_3 + num_4);

    return 0;
}