

/* >>> Print Numbers From 1 To 100 Program ( Version 1 ) <<< */

/* >>> A program that prints numbers from 1 to 100. <<< */

#include <stdio.h>

int main()
{
    printf("\n\nYou welcome in Print Numbers From 1 To 100 Program ( Version 1 ) ..\n\n");

    int i;

    for (i = 1; i <= 100; i++)
    {
        printf("%d\n", i);
    }
    printf("\n\n");

    return 0;
}