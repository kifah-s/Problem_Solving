

/* >>> Doubles Value Using LEFT SHIFT Program ( Version 1 ) <<< */

/* >>> Program multiplies the value of a variable using (using the Left Shift). <<< */

#include <stdio.h>
#include <string.h>

int main()
{
    printf("\n\nYou welcome in Doubles Value Using LEFT SHIFT Program ( Version 1 ) ..\n\n");

    int num = 5;
    printf("Before Doubles Value : num = %d\n", num);

    num = num << 1;
    printf("After Doubles Value : num = %d\n\n", num);

    return 0;
}